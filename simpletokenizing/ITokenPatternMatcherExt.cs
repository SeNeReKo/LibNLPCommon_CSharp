using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibNLPCSharp.simpletokenizing
{

	public static class ITokenPatternMatcherExt
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public static IEnumerable<TokenPatternResult> MatchAll(this ITokenPatternMatcher m, TokenStream tokenStream)
		{
			while (tokenStream.HasMoreTokens) {
				TokenPatternResult r = m.TryEat(tokenStream);
				if (r != null) {
					yield return r;
				} else {
					tokenStream.Skip(1);
				}
			}
		}

	}

}
