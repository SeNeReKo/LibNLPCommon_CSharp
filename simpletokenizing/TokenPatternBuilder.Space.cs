using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibNLPCSharp.simpletokenizing
{

	public partial class TokenPatternBuilder
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		private static readonly LinearPatternSequence __SPACEPATTERN1 = new LinearPatternSequence(
			TokenPattern.MatchWord("SPC", false)
			);

		private static readonly LinearPatternSequence __SPACEPATTERN2 = new LinearPatternSequence(
			TokenPattern.MatchWord("SPACE", false)
			);

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public static TokenPattern __TryEatSpace(TokenStream tokenStream)
		{
			TokenPatternResult p;

			if ((p = __SPACEPATTERN1.TryEat(tokenStream)) != null) {
				return TokenPattern.MatchSpace();
			}

			if ((p = __SPACEPATTERN2.TryEat(tokenStream)) != null) {
				return TokenPattern.MatchSpace();
			}

			return null;
		}

	}

}
