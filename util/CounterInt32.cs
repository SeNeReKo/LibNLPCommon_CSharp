using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibNLPCSharp.util
{

	public class CounterInt32
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		private int v;

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		public CounterInt32()
		{
		}

		public CounterInt32(int initialValue)
		{
			this.v = initialValue;
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		public int Value
		{
			get {
				return v;
			}
			set {
				this.v = value;
			}
		}

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public void Set(int value)
		{
			this.v = value;
		}

		public void Increment()
		{
			v++;
		}

		public void Decrement()
		{
			v--;
		}

		public override string ToString()
		{
			return "" + v;
		}

	}

}
