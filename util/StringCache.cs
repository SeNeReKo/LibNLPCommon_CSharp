using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibNLPCSharp.util
{

	public class StringCache
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		Dictionary<string, string> cache;

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		public StringCache()
		{
			cache = new Dictionary<string, string>();
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		public string this[string word]
		{
			get {
				if (word == null) return null;
				if (word.Length == 0) return string.Empty;

				string s;
				if (cache.TryGetValue(word, out s)) return s;

				cache.Add(word, word);
				return word;
			}
		}

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public void Clear()
		{
			cache.Clear();
		}

	}

}
