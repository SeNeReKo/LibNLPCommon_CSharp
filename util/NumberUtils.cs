using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibNLPCSharp.util
{

	public class NumberUtils
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		private NumberUtils()
		{
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public static ulong ToLong(int hi, int lo)
		{
			ulong lfrom = ((ulong)((uint)hi)) << 32;
			ulong lto = (ulong)(uint)lo;
			return lfrom | lto;
		}

		public static int ToIntA(ulong v)
		{
			return (int)(v >> 32);
		}

		public static int ToIntB(ulong v)
		{
			return (int)(v);
		}

	}

}
