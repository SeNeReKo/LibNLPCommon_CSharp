using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibNLPCSharp.util
{

	public class ImplementationErrorException : Exception
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		public ImplementationErrorException()
		{
		}

		public ImplementationErrorException(string message)
			: base(message)
		{
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public override string ToString()
		{
			StringBuilder sb = new StringBuilder("ImplementationErrorException[");
			if (Message != null) sb.Append(Message);
			sb.Append(']');
			return sb.ToString();
		}

	}

}
