using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibNLPCSharp.util
{

	public static class DictionaryExt
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public static V GetCreate<K, V>(this Dictionary<K, V> map, K key)
			where V : new()
		{
			V v;
			if (!map.TryGetValue(key, out v)) {
				v = new V();
				map.Add(key, v);
			}
			return v;
		}

		public static V Get<K, V>(this Dictionary<K, V> map, K key)
		{
			V v;
			if (!map.TryGetValue(key, out v)) {
				return default(V);
			}
			return v;
		}

	}

}
