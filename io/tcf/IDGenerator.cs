using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibNLPCSharp.io.tcf
{

	public class IDGenerator
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		private string prefix;

		private int counterValue = 0;

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		public IDGenerator(string prefix)
		{
			this.prefix = prefix;
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public void Reset()
		{
			counterValue = 0;
		}

		public string GetNext()
		{
			string s = prefix + counterValue;
			counterValue++;
			return s;
		}

	}

}
