using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibNLPCSharp.io.tcf
{

	public class TCFTextSpan
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		private String type;
		private String text;

		public TCFToken Begin;
		public TCFToken End;

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		public TCFTextSpan(TCFToken begin, TCFToken end, string type, string text)
		{
			if (begin == null) throw new Exception("No begin token specified!");
			if (end == null) throw new Exception("No end token specified!");
			if (type == null) throw new Exception("No type specified!");
			if (text == null) throw new Exception("No text specified!");

			this.Begin = begin;
			this.End = end;

			this.type = type;
			this.text = text;
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

	}

}
