using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibNLPCSharp.io.tcf
{

	public class TCFLemma
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		private string id;

		public TCFToken[] Tokens;
		public String Text;

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		public TCFLemma(string id, TCFToken[] tokens, string text)
		{
			__SetID(id);
			this.Tokens = tokens;
			this.Text = text;
		}

		/**
		 * Constructor.
		 */
		public TCFLemma(TCFToken[] tokens, string text)
		{
			this.Tokens = tokens;
			this.Text = text;
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		public string ID
		{
			get {
				return id;
			}
			set {
				__SetID(value);
			}
		}

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		private void __SetID(string id)
		{
			if ((id == null) || (id.Length == 0) || (id.Trim().Length != id.Length))
				throw new Exception("Invalid ID specified!");
			this.id = id;
		}

	}

}
