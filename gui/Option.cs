using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibNLPCSharp.gui
{

	public class Option
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		public Option(string key, string description)
		{
			Key = key;
			Description = description;
		}

		public Option(string key, string description, bool bIsChecked)
		{
			Key = key;
			Description = description;
			IsChecked = bIsChecked;
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		public string Key
		{
			get;
			private set;
		}

		public string Description
		{
			get;
			private set;
		}

		public bool IsChecked
		{
			get;
			set;
		}

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public override string ToString()
		{
			return "Option[" + Key + ": " + IsChecked + "]";
		}

	}

}
